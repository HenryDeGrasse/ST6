/**
 * Generate MD3 Dynamic Color palette from seed color #2563eb
 * Uses HCT color space for perceptually uniform, contrast-safe tokens.
 */
import {
  argbFromHex,
  hexFromArgb,
  themeFromSourceColor,
  customColor,
} from "@material/material-color-utilities";

const SEED = "#2563eb";

// Custom semantic colors to blend with the seed
const CUSTOM_COLORS = [
  { name: "success", value: "#059669", blend: true },
  { name: "warning", value: "#d97706", blend: true },
  { name: "error",   value: "#dc2626", blend: true },
  { name: "carryForward", value: "#7c3aed", blend: true },
];

const seedArgb = argbFromHex(SEED);
const theme = themeFromSourceColor(seedArgb, CUSTOM_COLORS.map(c => ({
  value: argbFromHex(c.value),
  name: c.name,
  blend: c.blend,
})));

function printPalette(label, scheme) {
  const keys = [
    "primary", "onPrimary", "primaryContainer", "onPrimaryContainer",
    "secondary", "onSecondary", "secondaryContainer", "onSecondaryContainer",
    "tertiary", "onTertiary", "tertiaryContainer", "onTertiaryContainer",
    "error", "onError", "errorContainer", "onErrorContainer",
    "background", "onBackground",
    "surface", "onSurface",
    "surfaceVariant", "onSurfaceVariant",
    "outline", "outlineVariant",
    "shadow", "scrim",
    "inverseSurface", "inverseOnSurface", "inversePrimary",
    "surfaceDim", "surfaceBright",
    "surfaceContainerLowest", "surfaceContainerLow", "surfaceContainer",
    "surfaceContainerHigh", "surfaceContainerHighest",
  ];

  console.log(`\n/* ─── ${label} ─── */`);
  for (const key of keys) {
    if (scheme[key] !== undefined) {
      console.log(`  --md3-${camelToKebab(key)}: ${hexFromArgb(scheme[key])};`);
    }
  }
}

function camelToKebab(str) {
  return str.replace(/([a-z])([A-Z])/g, "$1-$2").toLowerCase();
}

console.log("/* MD3 Dynamic Color Palette — Seed: " + SEED + " */");
console.log("/* Generated by scripts/generate-md3-palette.mjs */\n");

printPalette("Light Theme", theme.schemes.light.toJSON());
printPalette("Dark Theme", theme.schemes.dark.toJSON());

// Custom colors
for (const cc of theme.customColors) {
  console.log(`\n/* ─── Custom: ${cc.color.name} ─── */`);
  const lt = cc.light;
  const dk = cc.dark;
  const n = camelToKebab(cc.color.name);
  console.log(`  /* Light */`);
  console.log(`  --md3-${n}: ${hexFromArgb(lt.color)};`);
  console.log(`  --md3-on-${n}: ${hexFromArgb(lt.onColor)};`);
  console.log(`  --md3-${n}-container: ${hexFromArgb(lt.colorContainer)};`);
  console.log(`  --md3-on-${n}-container: ${hexFromArgb(lt.onColorContainer)};`);
  console.log(`  /* Dark */`);
  console.log(`  --md3-${n}-dark: ${hexFromArgb(dk.color)};`);
  console.log(`  --md3-on-${n}-dark: ${hexFromArgb(dk.onColor)};`);
  console.log(`  --md3-${n}-container-dark: ${hexFromArgb(dk.colorContainer)};`);
  console.log(`  --md3-on-${n}-container-dark: ${hexFromArgb(dk.onColorContainer)};`);
}

// Also output the tonal palette values for primary at key tones
console.log("\n/* ─── Primary Tonal Palette (key tones) ─── */");
const palettes = theme.palettes;
for (const tone of [0, 5, 10, 15, 20, 25, 30, 35, 40, 50, 60, 70, 80, 90, 92, 94, 95, 96, 98, 99, 100]) {
  console.log(`  --md3-primary-${tone}: ${hexFromArgb(palettes.primary.tone(tone))};`);
}

console.log("\n/* ─── Neutral Tonal Palette (key tones) ─── */");
for (const tone of [0, 4, 6, 10, 12, 17, 20, 22, 25, 30, 35, 40, 50, 60, 70, 80, 87, 90, 92, 94, 95, 96, 98, 99, 100]) {
  console.log(`  --md3-neutral-${tone}: ${hexFromArgb(palettes.neutral.tone(tone))};`);
}
